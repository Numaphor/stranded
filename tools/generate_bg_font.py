def write_4bpp_bmp(path, width, height, palette, pixels):
    """Write a 4-bit indexed BMP (no compression).

    palette  – list of (R,G,B) tuples, up to 16 entries
    pixels   – 2D array [row][col] of palette indices (top-to-bottom)
    """
    num_colors = 16
    row_stride = ((width * 4 + 31) // 32) * 4  # 4bpp row, 4-byte aligned
    pixel_data_size = row_stride * height
    palette_size = num_colors * 4
    header_size = 14 + 40 + palette_size
    file_size = header_size + pixel_data_size

    with open(path, 'wb') as f:
        # -- BMP file header (14 bytes) --
        f.write(b'BM')
        f.write(struct.pack('<I', file_size))
        f.write(struct.pack('<HH', 0, 0))
        f.write(struct.pack('<I', header_size))

        # -- DIB header (BITMAPINFOHEADER, 40 bytes) --
        f.write(struct.pack('<I', 40))
        f.write(struct.pack('<i', width))
        f.write(struct.pack('<i', height))     # positive = bottom-up
        f.write(struct.pack('<HH', 1, 4))      # planes=1, bpp=4
        f.write(struct.pack('<I', 0))           # no compression
        f.write(struct.pack('<I', pixel_data_size))
        f.write(struct.pack('<i', 0))           # x ppm
        f.write(struct.pack('<i', 0))           # y ppm
        f.write(struct.pack('<I', num_colors))
        f.write(struct.pack('<I', 0))           # important colors

        # -- Palette (16 entries, BGRA) --
        for i in range(num_colors):
            if i < len(palette):
                r, g, b = palette[i]
            else:
                r, g, b = (255, 0, 255)  # magenta = transparent fill
            f.write(struct.pack('BBBB', b, g, r, 0))

        # -- Pixel data (bottom-up) --
        for y in range(height - 1, -1, -1):
            row_bytes = bytearray(row_stride)
            for x in range(0, width, 2):
                hi = pixels[y][x] if x < width else 0
                lo = pixels[y][x + 1] if (x + 1) < width else 0
                row_bytes[x // 2] = ((hi & 0x0F) << 4) | (lo & 0x0F)
            f.write(row_bytes)


def main():
    os.makedirs(OUT_DIR, exist_ok=True)

    # Palette: index 0 = transparent (magenta), index 1 = white (text)
    palette = [
        (255, 0, 255),  # 0: transparent
        (255, 255, 255),  # 1: white text
    ]

    # Build pixel array: 96 characters stacked vertically
    pixels = [[0] * IMG_W for _ in range(IMG_H)]

    for char_code in range(32, 128):
        glyph = FONT_8x8.get(char_code, [0]*8)
        tile_y_offset = (char_code - 32) * TILE_H
        for row in range(TILE_H):
            byte = glyph[row]
            for col in range(TILE_W):
                if byte & (0x01 << col):
                    pixels[tile_y_offset + row][col] = 1  # white

    write_4bpp_bmp(OUT_BMP, IMG_W, IMG_H, palette, pixels)
    print(f'Wrote {OUT_BMP} ({IMG_W}x{IMG_H}, {NUM_CHARS} tiles)')

    # JSON for tiles
    with open(OUT_JSON, 'w') as f:
        f.write('{\n')
        f.write('    "type": "regular_bg_tiles",\n')
        f.write('    "bpp_mode": "bpp_4"\n')
        f.write('}\n')
    print(f'Wrote {OUT_JSON}')

    # Palette BMP: 16x16 single tile image with palette entry 1 = white
    pal_pixels = [[1] * 16 for _ in range(16)]
    write_4bpp_bmp(OUT_PAL_BMP, 16, 16, palette, pal_pixels)
    print(f'Wrote {OUT_PAL_BMP}')

    # Palette JSON
    with open(OUT_PAL_JSON, 'w') as f:
        f.write('{\n')
        f.write('    "type": "bg_palette",\n')
        f.write('    "bpp_mode": "bpp_4"\n')
        f.write('}\n')
    print(f'Wrote {OUT_PAL_JSON}')


if __name__ == '__main__':
    main()
